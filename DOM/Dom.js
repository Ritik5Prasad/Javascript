// The HTML DOM (Document Object Model) is a programming interface for web documents.
//  It represents the structure of an HTML document as a tree-like
//   structure, where each node represents a part of
//    the document, such as elements, attributes, and 
//    text. The DOM provides a way for scripts 
//    (like JavaScript) to dynamically access
//     and manipulate the content, structure,
//      and style of HTML documents.

// The DOM is a W3C (World Wide Web Consortium) standard.

// The DOM defines a standard for accessing documents:

// "The W3C Document Object Model (DOM) is a platform and language-neutral interface that allows programs and scripts to dynamically access and update the content, structure, and style of a document."

// The W3C DOM standard is separated into 3 different parts:

// Core DOM - standard model for all document types
// XML DOM - standard model for XML documents
// HTML DOM - standard model for HTML documents

